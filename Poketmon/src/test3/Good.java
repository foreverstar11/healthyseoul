package test3;

public class Good {
	int serial, good;

	public Good() {
		super();
	}

	public Good(int serial, int good) {
		super();
		this.serial = serial;
		this.good = good;
	}

	public int getSerial() {
		return serial;
	}

	public void setSerial(int serial) {
		this.serial = serial;
	}

	public int getGood() {
		return good;
	}

	public void setGood(int good) {
		this.good = good;
	}
	
}
